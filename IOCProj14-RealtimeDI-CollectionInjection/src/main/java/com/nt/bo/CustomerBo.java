package com.nt.bo;

public class CustomerBo {
	private int cno;
	private String cname;
	private String addr;
	private float pamt;
	private float itramt;
	public int getCno() {
		return cno;
	}
	public void setCno(int cno) {
		this.cno = cno;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public float getPamt() {
		return pamt;
	}
	public void setPamt(float pamt) {
		this.pamt = pamt;
	}
	public float getItramt() {
		return itramt;
	}
	public void setItramt(float itramt) {
		this.itramt = itramt;
	}
	
	

}
