package com.nt.vo;

public class CustomerVo {
	private int cno;
	private String cname;
	private String addr;
	private int pamt;
	private int time;
	private int rate;
	public int getCno() {
		return cno;
	}
	public void setCno(int cno) {
		this.cno = cno;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public int getPamt() {
		return pamt;
	}
	public void setPamt(int pamt) {
		this.pamt = pamt;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	public int getRate() {
		return rate;
	}
	public void setRate(int rate) {
		this.rate = rate;
	}
	
	

}
